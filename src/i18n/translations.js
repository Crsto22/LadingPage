export const translations = { es, qu, ay, zh, en };

export function resolveLanguage(language) {
	return languageOptions.some((item) => item.code === language) ? language : DEFAULT_LANGUAGE;
}

export function getLanguageMeta(language) {
	const resolved = resolveLanguage(language);
	return languageOptions.find((item) => item.code === resolved) ?? languageOptions[0];
}

export function translate(language, key, fallback = '') {
	const resolved = resolveLanguage(language);
	return translations[resolved]?.[key] ?? translations[DEFAULT_LANGUAGE]?.[key] ?? fallback;
}

export function getStoredLanguage() {
	if (typeof window === 'undefined') return DEFAULT_LANGUAGE;

	try {
		return resolveLanguage(window.localStorage.getItem(LANGUAGE_STORAGE_KEY));
	} catch {
		return DEFAULT_LANGUAGE;
	}
}

export function setStoredLanguage(language) {
	const resolved = resolveLanguage(language);

	if (typeof window !== 'undefined') {
		try {
			window.localStorage.setItem(LANGUAGE_STORAGE_KEY, resolved);
		} catch {
			// localStorage can be unavailable in private or restricted contexts.
		}

		window.dispatchEvent(new CustomEvent(LANGUAGE_CHANGE_EVENT, { detail: { language: resolved } }));
	}

	return resolved;
}
